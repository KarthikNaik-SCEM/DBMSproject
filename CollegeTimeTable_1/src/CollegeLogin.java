    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        if(cmbLogintype.getSelectedItem().equals("Student"))
        {
            String Uname= txtUsername.getText();
            String Password= txtPassword.getText();

            if(Uname.equals("admin")&&(Password.equals("12345"))){
                MainMenu m= new MainMenu();
                this.setVisible(false);
                m.setVisible(true);
            }
            else if(checkStudentUsers(Uname, Password)){
                StudentView s= new StudentView();
                this.setVisible(false);
                s.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "username or password doesn't match");
            }    
        }
        
        
        else if(cmbLogintype.getSelectedItem().equals("Faculty"))
        {
            String Uname= txtUsername.getText();
            String Password= txtPassword.getText();

            
            if(checkFacultyUsers(Uname, Password)){
                FacultyMainMenu f= new FacultyMainMenu();
                this.setVisible(false);
                f.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "username or password doesn't match");
            } 
        
        }
        
        else if(cmbLogintype.getSelectedItem().equals("Admin"))
        {
            String Uname= txtUsername.getText();
            String Password= txtPassword.getText();
            
            if(Uname.equals("admin")&&(Password.equals("12345"))){
                MainMenu m= new MainMenu();
                this.setVisible(false);
                m.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "invalid admin details");
            }
        }
            
    }//GEN-LAST:event_btnLoginActionPerformed

