    public boolean checkStudentUsers(String uname, String password)
    {
        try {
            Connection con=consetup.createCon();
            String q="select * from useraccounts;";
            Statement st= con.createStatement();
            ResultSet set = st.executeQuery(q);
            while(set.next())
            {
                if(uname.equals(set.getString(1))&&password.equals(set.getString(2)))
                {
                    return true;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return false;
    }
    
